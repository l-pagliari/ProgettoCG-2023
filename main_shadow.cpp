const unsigned int SCR_WIDTH = 800;
const unsigned int SCR_HEIGHT = 600;

// camera
Camera camera(glm::vec3(0.0f, 1.0f, 4.0f));
float lastX = SCR_WIDTH / 2.0f;
float lastY = SCR_HEIGHT / 2.0f;
bool firstMouse = true;
bool mouse_dragged = false;

// timing
float deltaTime = 0.0f; 
float lastFrame = 0.0f; 

// texture references
unsigned int diffuseMap;
unsigned int specularMap; 
unsigned int floorTexture; 
unsigned int transparentTexture; 

// model reference
Model * myModelPtr;

// lighting info
glm::vec3 lightPos(-3.0f, 4.0f, -2.0f);

int main()
{
    /* glfw setup */
    glfwInit();
    GLFWwindow *window = glfwCreateWindow(SCR_WIDTH, SCR_HEIGHT, "LearnOpenGL", NULL, NULL);
    if(window == NULL)
    {
        std::cout << "Failed to create GLFW window" << std::endl;
        glfwTerminate();
        return -1;
    }
    glfwMakeContextCurrent(window);
    glfwSetCursorPosCallback(window, mouse_callback);
    glfwSetMouseButtonCallback(window, mouse_button_callback);
    glfwSetScrollCallback(window, scroll_callback);

    // tell GLFW to capture our mouse
    glfwSetInputMode(window, GLFW_CURSOR, GLFW_CURSOR_DISABLED);

    /* glad setup */
    if(!gladLoadGLLoader((GLADloadproc)glfwGetProcAddress))
    {
        std::cout << "Failed to initialize GLAD" << std::endl;
        return -1;
    }

    // configure global opengl state
    // -----------------------------
    glEnable(GL_DEPTH_TEST);
    glEnable(GL_BLEND);
    glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);

    // // nota: alcune volte dopo la compilazione i path non coincidono
    // char tmp[256];
    // getcwd(tmp, 256);
    // cout << "Current working directory: " << tmp << endl;
    
    // build and compile shaders
    // ------------------------- 
    Shader shader("../shaders/shadowShader.vs", "../shaders/shadowShader.fs");
    Shader simpleDepthShader("../shaders/shadowMappingDepth.vs", "../shaders/shadowMappingDepth.fs");
    Shader skyboxShader("../shaders/skybox.vs", "../shaders/skybox.fs");

    // load models
    // -----------
    // nota: funziona ma allunga tantissimo i tempi di caricamento
    Model myModel("../objects/boot/sh_catWorkBoot.obj");
    myModelPtr = &myModel;
    
    // configure depth map FBO
    // -----------------------
    const unsigned int SHADOW_WIDTH = 1024, SHADOW_HEIGHT = 1024;
    unsigned int depthMapFBO;
    glGenFramebuffers(1, &depthMapFBO);
    // create depth texture
    unsigned int depthMap;
    glGenTextures(1, &depthMap);
    glBindTexture(GL_TEXTURE_2D, depthMap);
    glTexImage2D(GL_TEXTURE_2D, 0, GL_DEPTH_COMPONENT, SHADOW_WIDTH, SHADOW_HEIGHT, 0, GL_DEPTH_COMPONENT, GL_FLOAT, NULL);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
    // setting per l'over sampling
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_BORDER);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_BORDER);
    float borderColor[] = { 1.0, 1.0, 1.0, 1.0 };
    glTexParameterfv(GL_TEXTURE_2D, GL_TEXTURE_BORDER_COLOR, borderColor);
    // attach depth texture as FBO's depth buffer
    glBindFramebuffer(GL_FRAMEBUFFER, depthMapFBO);
    glFramebufferTexture2D(GL_FRAMEBUFFER, GL_DEPTH_ATTACHMENT, GL_TEXTURE_2D, depthMap, 0);
    glDrawBuffer(GL_NONE);
    glReadBuffer(GL_NONE);
    glBindFramebuffer(GL_FRAMEBUFFER, 0);

    // load textures
    // -------------
    diffuseMap = loadTexture("../textures/container.png");
    specularMap = loadTexture("../textures/container_specular.png");
    floorTexture = loadTexture("../textures/metal.png");
    transparentTexture = loadTexture("../textures/window.png");

    std::vector<std::string> faces
    {
        "../textures/skybox/xpos.jpg", //right
        "../textures/skybox/xneg.jpg", //left
        "../textures/skybox/ypos.jpg", //top
        "../textures/skybox/yneg.jpg", //bottom
        "../textures/skybox/zpos.jpg", //front
        "../textures/skybox/zneg.jpg"  //back
    };
    unsigned int cubemapTexture = loadCubemap(faces);

    // transparent windows locations
    // -----------------------------
    std::vector<glm::vec3> windows 
    {
        glm::vec3(-2.5f, 0.0f, -0.48f),
        glm::vec3( 2.5f, 0.2f,  0.51f),
        glm::vec3(-3.3f, 0.3f, -2.3f),
        glm::vec3 (0.2f, 0.4f, 2.6f)
    };

    // shader configuration
    // --------------------
    shader.use();
    shader.setInt("texture_diffuse1", 0);
    shader.setInt("texture_specular1", 1);
    shader.setInt("shadowMap", 2);

    skyboxShader.use();
    skyboxShader.setInt("skybox", 0);

    /* render loop */
    while(!glfwWindowShouldClose(window))
    {
        // per-frame time logic
        // --------------------
        float currentFrame = static_cast<float>(glfwGetTime());
        deltaTime = currentFrame - lastFrame;
        lastFrame = currentFrame;

        // input
        // -----
        processInput(window);

        // sort the transparent windows before rendering
        // ---------------------------------------------
        std::map<float, glm::vec3> sorted;
        for (unsigned int i = 0; i < windows.size(); i++)
        {
            float distance = glm::length(camera.Position - windows[i]);
            sorted[distance] = windows[i];
        }

        // render
        // ------
        glClearColor(0.1f, 0.1f, 0.1f, 1.0f);
        glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

        // render depth of scene to texture (from light's perspective)
        // -----------------------------------------------------------
        glViewport(0, 0, SHADOW_WIDTH, SHADOW_HEIGHT);
        glBindFramebuffer(GL_FRAMEBUFFER, depthMapFBO);
        glClear(GL_DEPTH_BUFFER_BIT);
        // light space transformation
        glm::mat4 lightProjection, lightView;
        glm::mat4 lightSpaceMatrix;
        float near_plane = 1.0f, far_plane = 30.0f;
        // directional light
        lightProjection = glm::ortho(-10.0f, 10.0f, -10.0f, 10.0f, near_plane, far_plane);
        lightView = glm::lookAt(lightPos, glm::vec3(0.0f), glm::vec3(0.0, 1.0, 0.0));
        lightSpaceMatrix = lightProjection * lightView;
        // render scene from light's point of view
        simpleDepthShader.use();
        simpleDepthShader.setMat4("lightSpaceMatrix", lightSpaceMatrix);
        renderScene(simpleDepthShader);
        glBindFramebuffer(GL_FRAMEBUFFER, 0);

        // reset viewport
        glViewport(0, 0, SCR_WIDTH, SCR_HEIGHT);
        glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
        
        // render scene as normal using the generated depth/shadow map  
        // -----------------------------------------------------------
        glActiveTexture(GL_TEXTURE2);
        glBindTexture(GL_TEXTURE_2D, depthMap);
        shader.use();
        shader.setMat4("lightSpaceMatrix", lightSpaceMatrix);
        renderScene(shader);

        // draw skybox as last
        skyboxShader.use();
        glm::mat4 projection = glm::perspective(glm::radians(camera.Zoom), (float)SCR_WIDTH / (float)SCR_HEIGHT, 0.1f, 100.0f);
        glm::mat4 view = glm::mat4(glm::mat3(camera.GetViewMatrix())); 
        skyboxShader.setMat4("view", view);
        skyboxShader.setMat4("projection", projection);
        // skybox cube
        glActiveTexture(GL_TEXTURE0);
        glBindTexture(GL_TEXTURE_CUBE_MAP, cubemapTexture);
        renderSkybox();
         
        // glfw: swap buffers and poll IO events (keys pressed/released, mouse moved etc.)
        // -------------------------------------------------------------------------------
        glfwSwapBuffers(window);
        glfwPollEvents();
    }
    
    glfwTerminate();
    return 0;   
}

void renderScene(Shader &shader) {
        
        shader.use();
        glm::mat4 model;
        
        glm::mat4 projection = glm::perspective(glm::radians(camera.Zoom), (float)SCR_WIDTH / (float)SCR_HEIGHT, 0.1f, 100.0f);
        glm::mat4 view = camera.GetViewMatrix();
        shader.setMat4("projection", projection);
        shader.setMat4("view", view);
        
        // lighting settings
        shader.setVec3("viewPos", camera.Position);
        shader.setVec3("dirLight.position", lightPos);
        shader.setVec3("dirLight.ambient", 0.2f, 0.2f, 0.2f);
        shader.setVec3("dirLight.diffuse", 0.5f, 0.5f, 0.5f);
        shader.setVec3("dirLight.specular",1.0f, 1.0f, 1.0f);
        
        shader.setVec3("spotLight.position", camera.Position);
        shader.setVec3("spotLight.direction", camera.Front);
        shader.setVec3("spotLight.ambient", 0.0f, 0.0f, 0.0f);
        shader.setVec3("spotLight.diffuse", 0.9f, 0.9f, 0.9f);
        shader.setVec3("spotLight.specular",1.0f, 1.0f, 1.0f);
        shader.setFloat("spotLight.cutOff", glm::cos(glm::radians(7.5f)));
        shader.setFloat("spotLight.outerCutOff", glm::cos(glm::radians(12.5f)));

        // boxes
        glActiveTexture(GL_TEXTURE0);
        glBindTexture(GL_TEXTURE_2D, diffuseMap);
        glActiveTexture(GL_TEXTURE1);
        glBindTexture(GL_TEXTURE_2D, specularMap);
        glm::vec3 cubePositions[] = {
            glm::vec3( 0.0f,  0.0f,  0.0f),
            glm::vec3( 2.5f,  1.0f, -3.5f),
            glm::vec3(-3.5f,  0.5f, -5.5f)
        };
        for (unsigned int i = 0; i < 3; i++)
        {
            // calculate the model matrix for each object and pass it to shader before drawing
             model = glm::mat4(1.0f);
            model = glm::scale(model, glm::vec3(0.5f));
            model = glm::translate(model, cubePositions[i]);
            shader.setMat4("model", model);
            renderCube();
        }

        // boot model
        if(myModelPtr != NULL) {
            model = glm::mat4(1.0f);
            model = glm::translate(model, glm::vec3(0.0f, 0.5f, 0.0f)); 
            model = glm::scale(model, glm::vec3(2.0f, 2.0f, 2.0f)); 
            shader.setMat4("model", model);
            myModelPtr->Draw(shader);
        }

        // floor
        glActiveTexture(GL_TEXTURE0);
        glBindTexture(GL_TEXTURE_2D, floorTexture);
        shader.setVec3("dirLight.specular",0.0f, 0.0f, 0.0f);
        model = glm::mat4(1.0f);
        shader.setMat4("model", model);
        renderPlane();

        // transparent windows locations
        // -----------------------------
        std::vector<glm::vec3> windows{
            glm::vec3(-2.5f, 0.0f, -0.48f),
            glm::vec3(2.5f, 0.2f, 0.51f),
            glm::vec3(-3.3f, 0.3f, -2.3f),
            glm::vec3(0.2f, 0.4f, 2.6f)};

        // sort the transparent windows before rendering
        // ---------------------------------------------
        std::map<float, glm::vec3> sorted;
        for (unsigned int i = 0; i < windows.size(); i++)
        {
            float distance = glm::length(camera.Position - windows[i]);
            sorted[distance] = windows[i];
        }
        // windows (from furthest to nearest)
        glActiveTexture(GL_TEXTURE0);
        glBindTexture(GL_TEXTURE_2D, transparentTexture);
        for (std::map<float, glm::vec3>::reverse_iterator it = sorted.rbegin(); it != sorted.rend(); ++it)
        {
            model = glm::mat4(1.0f);
            model = glm::translate(model, it->second);
            model = glm::scale(model, glm::vec3(0.5f));
            shader.setMat4("model", model);
            renderWindow();
        }
}

